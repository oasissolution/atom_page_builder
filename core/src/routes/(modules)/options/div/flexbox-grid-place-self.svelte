<script>

    import "../../../../app.css";
    import Select from "../../../uicomponents/select.svelte";

    /**
     * @type boolean
     */
    export let loaded;

    /**
     * Actual class
     * @type string
     */
    export let classInput;

    /**
     * Actual part of class
     * @type string
     */
    export let property;


    /**
     * @typedef {Object} SelectOptions
     * @property {string} value
     * @property {string} name
     * @property {string} info
    */


    /**
     * Value of element
     * @type Array<SelectOptions>
     */
    let propertyOptions = [

        {name: "Default",   value: "",                     info: "Default"},
        {name: "Auto",      value: "place-self-auto",      info: "place-self: auto;\n\nUse 'Auto' to align an item based on the value of the containerâ€™s place-items property"},
        {name: "Start",     value: "place-self-start",     info: "place-self: start;\n\nUse 'Start' to align an item to the start on both axes"},
        {name: "Center",    value: "place-self-center",    info: "place-self: center;\n\nUse 'Center' to align an item at the center on both axes"},
        {name: "End",       value: "place-self-end",       info: "place-self: end;\n\nUse 'End' to align an item to the end on both axes"},
        {name: "Stretch",   value: "place-self-stretch",   info: "place-self: stretch;\n\nUse 'Stretch' to stretch an item on both axes"},

        // {name: "",     value: "",     info: ""},
    ];

    /**
     * List of all classes
     * @type Array<string>
     */
    let propertyList = [];

    propertyOptions.forEach(elm => {
        propertyList.push(elm.value);
    });

    import { createEventDispatcher } from 'svelte';

    const dispatch = createEventDispatcher();

    function updateClass() {
        dispatch('updateClass');
    }

    /**
     * Used for sub elements update control.
     * @type string
    */
    export let elementDataLoaded;

    $: loaded, (()=>{
        if(loaded == true) {
            elementDataLoaded.split(" ").forEach( cls => {

                var currentClass = cls.trim();

                propertyList.forEach( elm => {
                    if(elm === currentClass) property = elm;
                });


            });
        }
    })();

    $: property, (()=>{
        if(loaded == true) {

            var newClass = "";

            classInput.split(" ").forEach( cls => {

                if(propertyList.includes(cls.trim())){
                    ///Do nothing - If included then should be removed
                }else{
                    newClass += " " + cls.trim();
                }
            });

            classInput = newClass.trim() + " " + property;
            updateClass();
        }
    })();



</script>


<div class="w-full flex flex-row flex-grow justify-between h-8 align-middle items-center">
    <span title="Utilities for controlling how an individual item is justified and aligned at the same time.
    
Use when item needs to be differently justified and aligned of its container's 'Place Items' property.">Place Self</span>
    <Select options={propertyOptions} bind:value={property}/>
</div>
